
public class HelloNepal {

public static void main(String[] args) {
System.out.println("Hello Nepal!!");
 
        int x = 78, y = 7;
 
        System.out.println("Before Swap");
        System.out.println("x = " + x);
        System.out.println("y = " + y);
 
        int temp = x;
        x = y;
        y = temp;
 
        System.out.println("After swap");
        System.out.println("x = " + x);
        System.out.println("y = " + y);
    }
}